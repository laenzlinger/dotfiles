#!/usr/bin/env bash
set -euo pipefail

tinty_repos="${HOME}/.local/share/tinted-theming/tinty/repos/base16-waybar/colors"
dark_src="${tinty_repos}/base16-darktooth.css"
light_src="${tinty_repos}/base16-gruvbox-light-medium.css"
profiles_dir="${HOME}/.mozilla/firefox"
[[ -d "$profiles_dir" ]] || profiles_dir="${HOME}/.config/mozilla/firefox"

[[ -f "$dark_src" ]] || exit 0
[[ -f "$light_src" ]] || exit 0
[[ -d "$profiles_dir" ]] || exit 0

get() { sed -n "s/@define-color $1 \(#[0-9a-fA-F]*\);/\1/p" "$2"; }

gen_rules() {
    local src="$1"
    cat <<EOF
    --toolbar-bgcolor: $(get base01 "$src") !important;
    --toolbar-color: $(get base05 "$src") !important;
    --toolbar-field-background-color: $(get base00 "$src") !important;
    --toolbar-field-color: $(get base05 "$src") !important;
    --toolbar-field-border-color: $(get base02 "$src") !important;
    --toolbar-field-focus-background-color: $(get base00 "$src") !important;
    --toolbar-field-focus-color: $(get base05 "$src") !important;
    --lwt-toolbar-field-highlight: $(get base02 "$src") !important;
    --lwt-accent-color: $(get base00 "$src") !important;
    --lwt-text-color: $(get base05 "$src") !important;
    --tab-selected-bgcolor: $(get base01 "$src") !important;
    --tab-selected-textcolor: $(get base05 "$src") !important;
    --lwt-tab-text: $(get base04 "$src") !important;
    --arrowpanel-background: $(get base00 "$src") !important;
    --arrowpanel-color: $(get base05 "$src") !important;
    --arrowpanel-border-color: $(get base02 "$src") !important;
    --sidebar-background-color: $(get base00 "$src") !important;
    --sidebar-text-color: $(get base05 "$src") !important;
    --urlbar-box-bgcolor: $(get base00 "$src") !important;
EOF
}

gen_selectors() {
    local src="$1"
    cat <<EOF
#navigator-toolbox { background: $(get base00 "$src") !important; }
#nav-bar { background: $(get base01 "$src") !important; }
#TabsToolbar { background: $(get base00 "$src") !important; }
.tab-background[selected] { background: $(get base01 "$src") !important; }
#urlbar-background { background: $(get base00 "$src") !important; }
findbar { background: $(get base01 "$src") !important; color: $(get base05 "$src") !important; }
#sidebar-box, #sidebar-header { background: $(get base00 "$src") !important; color: $(get base05 "$src") !important; }
#sidebar { background: $(get base00 "$src") !important; color: $(get base05 "$src") !important; }
#PersonalToolbar { background: $(get base00 "$src") !important; color: $(get base05 "$src") !important; }
#PlacesToolbarItems toolbarbutton { color: $(get base05 "$src") !important; }
#sidebar-box *|*:root { --lwt-sidebar-background-color: $(get base00 "$src") !important; --lwt-sidebar-text-color: $(get base05 "$src") !important; }
#sidebar-main, #sidebar-main .sidebar-panel { background: $(get base00 "$src") !important; color: $(get base05 "$src") !important; }
menupopup, panel, .panel-arrowcontent, panelview, #appMenu-popup, #customizationui-widget-panel, #widget-overflow { background: $(get base00 "$src") !important; color: $(get base05 "$src") !important; }
menuitem, menu { color: $(get base05 "$src") !important; }
menuitem:hover, menu[_moz-menuactive] { background: $(get base02 "$src") !important; }
#PopupAutoComplete, .autocomplete-richlistbox, #urlbar .urlbarView { background: $(get base00 "$src") !important; color: $(get base05 "$src") !important; }
.urlbarView-row:hover, .urlbarView-row[selected] { background: $(get base02 "$src") !important; }
EOF
}

css="/* Base16 theme - generated by tinty hook */
/* Uses prefers-color-scheme for live dark/light switching */

#tabbrowser-tabs { --tab-min-height: 28px !important; }
#TabsToolbar { height: 28px !important; }

@media (prefers-color-scheme: dark) {
:root {
$(gen_rules "$dark_src")
}
$(gen_selectors "$dark_src")
}

@media (prefers-color-scheme: light) {
:root {
$(gen_rules "$light_src")
}
$(gen_selectors "$light_src")
}
"

content_css="/* Base16 theme for Firefox content areas */
@-moz-document url-prefix(\"chrome://browser/content/places/\") {
    @media (prefers-color-scheme: dark) {
        body, #places { background-color: $(get base00 "$dark_src") !important; color: $(get base05 "$dark_src") !important; }
        treechildren { background-color: $(get base00 "$dark_src") !important; color: $(get base05 "$dark_src") !important; }
    }
    @media (prefers-color-scheme: light) {
        body, #places { background-color: $(get base00 "$light_src") !important; color: $(get base05 "$light_src") !important; }
        treechildren { background-color: $(get base00 "$light_src") !important; color: $(get base05 "$light_src") !important; }
    }
}

@-moz-document url(\"about:blank\"), url(\"about:newtab\"), url(\"about:home\") {
    @media (prefers-color-scheme: dark) {
        body { background-color: $(get base00 "$dark_src") !important; }
    }
    @media (prefers-color-scheme: light) {
        body { background-color: $(get base00 "$light_src") !important; }
    }
}
"

for profile in "$profiles_dir"/*.default-release "$profiles_dir"/*.default; do
    [[ -d "$profile" ]] || continue
    mkdir -p "$profile/chrome"
    echo "$css" > "$profile/chrome/userChrome.css"
    echo "$content_css" > "$profile/chrome/userContent.css"
done
